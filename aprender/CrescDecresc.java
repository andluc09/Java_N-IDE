package com.aprender;

public class CrescDecresc {
 
  public static void main(String[] args){
    
    int cont = 0;
    
    for(cont=0; cont<=10; cont++){
      System.out.println(cont);
    }
    
    System.out.println("\n");
    
    for(cont=10; cont>=0; cont--){
      System.out.println(cont);
    }
  }
}
